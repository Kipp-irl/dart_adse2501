/* Dart Program to demonstrate relational operators
 * Author: Victor
 * Description: This program demonstrates relational operators in dart
 */
void main()
{
  // Variables to be used in the program
  int num1 = 5, num2 = 3;

  // Program header
  print("==== DART RELATIONAL OPERATORS ====");
  print("Comparison result between $num1 and $num2");
  print("-------------------------------------------------");
  print("$num1 > $num2 = ${num1 > num2}");
  print("$num1 >= $num2 = ${num1 >= num2}");
  print("$num1 < $num2 = ${num1 < num2}");
  print("$num1 <= $num2 = ${num1 <= num2}");
  print("$num1 != $num2: ${num1 != num2}");
  print("$num1 == $num1: ${num1 == num1}");
  print("$num1 != $num1: ${num1 != num1}");
  print("-------------------------------------------------");
  print("\n==== END OF DART RELATIONAL OPERATORS DEMONSTRATION ====");
}